
# 1. Print a string:

print("Hello, World!")

#Output:
# Hello, World!

# 2. Print multiple values:

print("Hello", "World", 2025)

# Output:
# Hello World 2025

# 3. Print numbers:

print(10)
print(3.14)

# Output:
# 10
# 3.14

# 4. Print with string concatenation:

name = "Alice"
print("Hello, " + name + "!")

# Output:
# Hello, Alice!


# 5. Print using f-strings (formatted strings):
age = 25
print(f"I am {age} years old.")

# Output:
# I am 25 years old.
# 6. Print with sep (separator) and end:

print("Python", "is", "fun", sep="-", end="!")

# Output:
# Python-is-fun!

